package kr.co.min.beans;

public class CarListBean {

	private int carsale_num;
	private String carsale_name;
	private int carsale_Category;
	private int carsale_Price;
	private int carsale_company;
	private String carsale_img;
	private String carsale_info;
	
	public int getCarsale_num() {
		return carsale_num;
	}
	public void setCarsale_num(int carsale_num) {
		this.carsale_num = carsale_num;
	}
	public String getCarsale_name() {
		return carsale_name;
	}
	public void setCarsale_name(String carsale_name) {
		this.carsale_name = carsale_name;
	}
	public int getCarsale_Category() {
		return carsale_Category;
	}
	public void setCarsale_Category(int carsale_Category) {
		this.carsale_Category = carsale_Category;
	}
	public int getCarsale_Price() {
		return carsale_Price;
	}
	public void setCarsale_Price(int carsale_Price) {
		this.carsale_Price = carsale_Price;
	}
	public int getCarsale_company() {
		return carsale_company;
	}
	public void setCarsale_company(int carsale_company) {
		this.carsale_company = carsale_company;
	}
	public String getCarsale_img() {
		return carsale_img;
	}
	public void setCarsale_img(String carsale_img) {
		this.carsale_img = carsale_img;
	}
	public String getCarsale_info() {
		return carsale_info;
	}
	public void setCarsale_info(String carsale_info) {
		this.carsale_info = carsale_info;
	}
	
}
